async function mcp(options: RuntimeCliOptions): Promise<RuntimeCliResult> {
  const cwd = options.cwd ?? process.cwd();
  const env = await runtimeEnv(cwd, options);
  await runApiagexMcpStdioServer({
    automationToken: env.APIAGEX_AUTOMATION_TOKEN,
    baseUrl: resolveAiBaseUrl(env, undefined),
  });
  return ok("");
}

async function ai(args: string[], options: RuntimeCliOptions): Promise<RuntimeCliResult> {
  const subcommand = args[0] ?? "--help";
  if (subcommand === "--help" || subcommand === "-h") return ok(renderAiHelp());
  if (subcommand === "context") return aiContext(args.slice(1), options);
  if (subcommand === "doctor") return aiDoctor(args.slice(1), options);
  if (subcommand === "token") return aiToken(args.slice(1), options);
  return fail(`Unknown apiagex ai command: ${subcommand}\n\n${renderAiHelp()}`);
}

function renderAiHelp(): string {
  return `apiagex ai

Usage:
  apiagex ai context  Create or refresh .apiagex/codex.md.
  apiagex ai token    Create a temporary automation token.
  apiagex ai doctor   Check local AI/Codex setup.

Options:
  --base-url <url>       Base URL. Default: APIAGEX_BASE_URL or HOST/PORT.
  --admin-token <token>  Owner session token for apiagex ai token.
  --name <name>          Token name. Default: Codex setup.
  --ttl-minutes <mins>   Token TTL. Default: 60.
  --scopes <list>        Comma-separated automation scopes.
`;
}

async function aiContext(args: string[], options: RuntimeCliOptions): Promise<RuntimeCliResult> {
  const parsed = parseAiArgs(args);
  if (typeof parsed === "string") return fail(parsed);
  const cwd = options.cwd ?? process.cwd();
  const env = await runtimeEnv(cwd, options);
  const contextPath = join(cwd, ".apiagex", "codex.md");
  await mkdir(join(cwd, ".apiagex"), { recursive: true });
  await writeFile(contextPath, renderRuntimeCodexContext(resolveAiBaseUrl(env, parsed.baseUrl)), "utf8");
  return ok(`Wrote ${contextPath}\nKeep real tokens in APIAGEX_AUTOMATION_TOKEN or APIAGEX_ADMIN_TOKEN, never in source files.\n`);
}

async function aiDoctor(args: string[], options: RuntimeCliOptions): Promise<RuntimeCliResult> {
  const parsed = parseAiArgs(args);
  if (typeof parsed === "string") return fail(parsed);
  const cwd = options.cwd ?? process.cwd();
  const env = await runtimeEnv(cwd, options);
  const contextPath = join(cwd, ".apiagex", "codex.md");
  const contextExists = await fileExists(contextPath);
  const lines = [
    "Apiagex AI setup:",
    `- Base URL: ${resolveAiBaseUrl(env, parsed.baseUrl)}`,
    `- Context file: ${contextExists ? "found" : "missing"} (${contextPath})`,
    `- APIAGEX_AUTOMATION_TOKEN: ${env.APIAGEX_AUTOMATION_TOKEN ? "set" : "not set"}`,
    `- APIAGEX_ADMIN_TOKEN: ${env.APIAGEX_ADMIN_TOKEN ? "set" : "not set"}`,
    "No token values are printed by doctor.",
    "",
  ];
  return ok(lines.join("\n"));
}

async function aiToken(args: string[], options: RuntimeCliOptions): Promise<RuntimeCliResult> {
  const parsed = parseAiArgs(args);
  if (typeof parsed === "string") return fail(parsed);
  const cwd = options.cwd ?? process.cwd();
  const env = await runtimeEnv(cwd, options);
  const adminToken = parsed.adminToken ?? env.APIAGEX_ADMIN_TOKEN;
  if (!adminToken) return fail("Set APIAGEX_ADMIN_TOKEN or pass --admin-token to create an automation token.\n");
  const baseUrl = resolveAiBaseUrl(env, parsed.baseUrl);
  const response = await fetch(`${baseUrl}/api/admin/automation-tokens`, {
    body: JSON.stringify({
      name: parsed.name ?? "Codex setup",
      scopes: parsed.scopes,
      ttlMinutes: parsed.ttlMinutes ?? 60,
    }),
    headers: {
      authorization: `Bearer ${adminToken}`,
      "content-type": "application/json",
    },
    method: "POST",
  });
  const body: unknown = await response.json().catch(async () => ({ error: await response.text() }));
  if (!response.ok || !isAutomationTokenCreateResponse(body)) {
    return fail(`Automation token creation failed (${response.status}): ${JSON.stringify(body)}\n`);
  }
  return ok([
    "Created automation token.",
    `export APIAGEX_AUTOMATION_TOKEN=${body.token}`,
    "Warning: do not commit this token or write it into source files.",
    `Expires at: ${body.tokenRecord.expiresAt}`,
    `Scopes: ${body.tokenRecord.scopes.join(", ")}`,
    "",
  ].join("\n"));
}

function parseAiArgs(args: string[]): {
  adminToken?: string;
  baseUrl?: string;
  name?: string;
  scopes?: string[];
  ttlMinutes?: number;
} | string {
  const parsed: { adminToken?: string; baseUrl?: string; name?: string; scopes?: string[]; ttlMinutes?: number } = {};
  for (let index = 0; index < args.length; index += 1) {
    const arg = args[index] ?? "";
    if (arg === "--base-url") {
      const value = args[index + 1];
      if (!value) return "Use --base-url <url>.";
      parsed.baseUrl = stripTrailingSlash(value);
      index += 1;
    } else if (arg === "--admin-token") {
      const value = args[index + 1];
      if (!value) return "Use --admin-token <token>.";
      parsed.adminToken = value;
      index += 1;
    } else if (arg === "--name") {
      const value = args[index + 1];
      if (!value) return "Use --name <name>.";
      parsed.name = value;
      index += 1;
    } else if (arg === "--ttl-minutes") {
      const value = args[index + 1];
      if (!value || !Number.isInteger(Number(value))) return "Use --ttl-minutes <minutes>.";
      parsed.ttlMinutes = Number(value);
      index += 1;
    } else if (arg === "--scopes") {
      const value = args[index + 1];
      if (!value) return "Use --scopes <comma-separated-scopes>.";
      parsed.scopes = value.split(",").map((scope) => scope.trim()).filter(Boolean);
      index += 1;
    } else return `Unknown apiagex ai option: ${arg}`;
  }
  return parsed;
}

async function runtimeEnv(cwd: string, options: RuntimeCliOptions): Promise<Record<string, string>> {
  return { ...await readProjectEnv(cwd), ...process.env, INIT_CWD: cwd, ...options.env };
}

function resolveAiBaseUrl(env: Record<string, string>, baseUrl: string | undefined): string {
  if (baseUrl) return stripTrailingSlash(baseUrl);
  if (env.APIAGEX_BASE_URL) return stripTrailingSlash(env.APIAGEX_BASE_URL);
  return `http://${env.HOST ?? "127.0.0.1"}:${env.PORT ?? "4000"}`;
}

function stripTrailingSlash(value: string): string {
  return value.replace(/\/+$/, "");
}

async function fileExists(path: string): Promise<boolean> {
  try {
    await access(path);
    return true;
  } catch (error) {
    if (typeof error === "object" && error && "code" in error && error.code === "ENOENT") return false;
    throw error;
  }
}

