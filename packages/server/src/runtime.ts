function isAutomationTokenCreateResponse(body: unknown): body is {
  token: string;
  tokenRecord: { expiresAt: string; scopes: string[] };
} {
  return typeof body === "object"
    && body !== null
    && "token" in body
    && typeof body.token === "string"
    && "tokenRecord" in body
    && typeof body.tokenRecord === "object"
    && body.tokenRecord !== null
    && "expiresAt" in body.tokenRecord
    && typeof body.tokenRecord.expiresAt === "string"
    && "scopes" in body.tokenRecord
    && Array.isArray(body.tokenRecord.scopes)
    && body.tokenRecord.scopes.every((scope) => typeof scope === "string");
}

async function openRuntimeDatabase(config: { databasePath: string; databaseProvider: string; databaseUrl?: string }): Promise<ApiagexDatabase> {
  if (config.databaseProvider === "postgres") return openPostgresAdapter(config.databaseUrl, { migrate: true });
  if (config.databaseProvider === "mysql") return openMySqlAdapter(config.databaseUrl, { migrate: true });
  return openMigratedSqliteAdapter(config.databasePath);
}

async function generateTypes(args: string[], options: RuntimeCliOptions): Promise<RuntimeCliResult> {
  const parsed = parseTypesArgs(args);
  if (typeof parsed === "string") return fail(parsed);
  const cwd = options.cwd ?? process.cwd();
  const env = { ...await readProjectEnv(cwd), ...process.env, INIT_CWD: cwd, ...options.env };
  const config = resolveLocalServerConfig(env, cwd);
  await ensureLocalServerPaths(config);
  const database = await openRuntimeDatabase(config);
  try {
    const result = await generateApiagexTypes(database, {
      cwd,
      ...(parsed.outFile === undefined ? {} : { outFile: parsed.outFile }),
    });
    return ok(`Generated ${result.schemaCount} schema type${result.schemaCount === 1 ? "" : "s"} at ${result.outFile}\n`);
  } finally {
    await database.close();
  }
}

function parseTypesArgs(args: string[]): { outFile?: string } | string {
  const parsed: { outFile?: string } = {};
  for (let index = 0; index < args.length; index += 1) {
    const arg = args[index] ?? "";
    if (arg === "--out" || arg === "-o") {
      const outFile = args[index + 1];
      if (!outFile) return "Use apiagex types --out <path>.";
      parsed.outFile = outFile;
      index += 1;
    } else if (arg.startsWith("-")) return `Unknown apiagex types option: ${arg}`;
    else return `Unknown apiagex types argument: ${arg}`;
  }
  return parsed;
}

async function readProjectEnv(cwd: string): Promise<Record<string, string>> {
  try {
    return parseDotEnv(await readFile(join(cwd, ".env"), "utf8"));
  } catch (error) {
    if (typeof error === "object" && error && "code" in error && error.code === "ENOENT") return {};
    throw error;
  }
}

function parseDotEnv(content: string): Record<string, string> {
  const env: Record<string, string> = {};
  for (const rawLine of content.split(/\r?\n/)) {
    const line = rawLine.trim();
    if (!line || line.startsWith("#")) continue;
    const separator = line.indexOf("=");
    if (separator === -1) continue;
    const key = line.slice(0, separator).trim();
    if (!key) continue;
    env[key] = unquoteEnvValue(line.slice(separator + 1).trim());
  }
  return env;
}

function unquoteEnvValue(value: string): string {
  if ((value.startsWith('"') && value.endsWith('"')) || (value.startsWith("'") && value.endsWith("'"))) {
    return value.slice(1, -1);
  }
  return value;
}

function databaseLocationLine(config: { databasePath: string; databaseProvider: string; databaseUrl?: string }): string {
  if (config.databaseProvider === "postgres") return "Apiagex database provider: postgres";
  if (config.databaseProvider === "mysql") return "Apiagex database provider: mysql";
  return `Apiagex database path: ${config.databasePath}`;
}

function startupErrorMessage(host: string, port: number, error: unknown): string {
  const code = typeof error === "object" && error && "code" in error ? String(error.code) : "";
  const detail = error instanceof Error ? error.message : String(error);
  if (code === "EADDRINUSE") {
    return `Apiagex failed to start: http://${host}:${port} is already in use. Stop the other server or set PORT to another value.`;
  }
  return `Apiagex failed to start on http://${host}:${port}: ${detail}`;
}

async function bootstrapInitialOwner(database: ApiagexDatabase, owner: { email: string; password: string }): Promise<void> {
  try {
    await bootstrapOwner(database, owner);
    console.log(`Apiagex owner created: ${owner.email}`);
  } catch (error) {
    const message = error instanceof Error ? error.message : "OWNER_BOOTSTRAP_FAILED";
    if (message !== "OWNER_ALREADY_BOOTSTRAPPED") throw error;
    console.log("Apiagex owner already exists; skipping owner bootstrap.");
  }
}

async function smoke(options: RuntimeCliOptions): Promise<RuntimeCliResult> {
  const server = createServer();
  const response = await server.inject({ method: "GET", url: "/api/health" });
  await server.close();
  if (response.statusCode !== 200 || response.json().ok !== true) {
    return fail("Apiagex smoke failed: /api/health did not return ok.\n");
  }
  const cwd = options.cwd ?? process.cwd();
  return ok(`Apiagex smoke passed in ${cwd}.\n`);
}

function ok(stdout: string): RuntimeCliResult {
  return { code: 0, stderr: "", stdout };
}

function fail(stderr: string): RuntimeCliResult {
  return { code: 1, stderr, stdout: "" };
}
